const std = @import("std");
pub fn main() !void {
    const compressed = [_]u8{ 
        0x1f, 0x8b, 0x08, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x03, 
        0xf3, 0x48, 0xcd, 0xc9, 0xc9, 0x57, 0x50, 0x2c, 0x4f, 0x51, 0x54, 0x04, 0x00, 
        0x41, 0xe1, 0xdb, 0x25, 0x0a, 0x00, 0x00, 0x00 
    };
    var fbs = std.io.fixedBufferStream(&compressed);
    var decompressor = std.compress.gzip.decompressor(fbs.reader());
    var out_buf: [1024]u8 = undefined;
    const len = try decompressor.reader().readAll(&out_buf);
    std.debug.print("Decompressed: {s}\n", .{out_buf[0..len]});
}